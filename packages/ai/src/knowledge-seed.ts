interface SupabaseLike {
  from(table: string): any;
}

/**
 * 将初始知识数据入库
 * 使用 upsert 确保幂等性（重复执行不会重复插入）
 */
export async function seedKnowledge(
  db: SupabaseLike,
  options?: { chunkSize?: number; overlap?: number },
): Promise<{ documentsInserted: number; chunksInserted: number }> {
  const { chunkSize = 500, overlap = 50 } = options ?? {};

  let documentsInserted = 0;
  let chunksInserted = 0;

  for (const doc of SEED_KNOWLEDGE) {
    try {
      // 1. 检查是否已存在（按 title 查重）
      const { data: existing } = await db
        .from('knowledge_documents')
        .select('id')
        .eq('title', doc.title)
        .maybeSingle();

      if (existing) {
        console.log(`[Seed] Skip "${doc.title}" (already exists)`);
        continue;
      }

      // 2. 插入文档
      const { data: docRow, error: docError } = await db
        .from('knowledge_documents')
        .insert({
          title: doc.title,
          collection: doc.collection,
          content: doc.content,
          source_url: doc.sourceUrl ?? null,
          metadata: doc.metadata ?? {},
        })
        .select('id')
        .single();

      if (docError || !docRow) {
        console.error(`[Seed] Insert document error (${doc.title}):`, docError?.message);
        continue;
      }

      documentsInserted++;

      // 3. 分块
      const chunks = chunkText(doc.content, { chunkSize, overlap });

      // 4. 插入分块
      const chunkRows = chunks.map((chunk) => ({
        document_id: docRow.id,
        chunk_index: chunk.metadata.chunkIndex,
        content: chunk.content,
        metadata: {
          ...chunk.metadata,
          ...(doc.metadata ?? {}),
        },
      }));

      if (chunkRows.length > 0) {
        const { error: chunkError } = await db
          .from('knowledge_chunks')
          .insert(chunkRows);

        if (chunkError) {
          console.error(`[Seed] Insert chunks error (${doc.title}):`, chunkError.message);
        } else {
          chunksInserted += chunkRows.length;
          console.log(`[Seed] ✓ "${doc.title}": ${chunkRows.length} chunks`);
        }
      }
    } catch (err) {
      console.error(`[Seed] Exception for "${doc.title}":`, err);
    }
  }

  console.log(`\n[Seed] Complete: ${documentsInserted} documents, ${chunksInserted} chunks`);
  return { documentsInserted, chunksInserted };
}
